    echo "{$z} <br>";

    // perkalian
    $z = $x * $y;
    echo "{$z} <br>";

    // pembagian
    $z = $x / $y;
    echo "{$z} <br>";

    // modulus
    $z = $x % $y;
    echo "{$z} <br>";

    // eksponensial
    $z = $x ** $y;
    echo "{$z} <br>";


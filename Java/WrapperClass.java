public class WrapperClass
{
    public static void main (String [] args)
    {
        /*
            wrapper class = class untuk mengubah tipe data primitif ke tipe data obje
            
            autoboxing    = mengubah tipe data primitif ke tipe data objek
            unboxing      = mengubah tipe data objek ke tipe data primitif
        */

        Boolean a = true;
        Character b = '@';
        Integer c = 123;
        Double d = 3.14;
        String e = "Dzul";

        System.out.println (a);
        System.out.println (b);
        System.out.println (c);
        System.out.println (d);
        System.out.println (e);
    }    
}
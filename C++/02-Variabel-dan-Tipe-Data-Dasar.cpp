#include <iostream>

int main ()
{
    // integer
    int x = 5;
    std::cout << x << "\n";

    // double
    double desimal = 0.5;
    std::cout << desimal << "\n";

    // char
    char simbol = 'x';
    std::cout << simbol << "\n";

    // boolean (true atau false)
    bool konfirmasi = true;
    std::cout << konfirmasi << "\n";


    return 0;
}
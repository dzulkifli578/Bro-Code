#include <iostream>
#include <cmath>

int main ()
{
    double x = 3.14, y = 4;

    std::cout << std::max (x,y);
    std::cout << std::min (x,y);
    std::cout << pow (5, 5);
    std::cout << sqrt (25);
    std::cout << abs (-5);
    std::cout << round (x);
    std::cout << ceil (x);
    std::cout << floor (x);
    
    return 0;
}  